import type { StudentState } from "@ai-tutor/shared"

export const buildAiPlannerPrompt = (state: StudentState, validationFeedback?: string[]) => `Ты AI-тьютор и аналитик учебного прогресса.

Сформируй персональный план обучения на основе student_state.
Твоя главная роль: объяснить ситуацию студента, риски, прогноз и стратегию прохождения. Календарь должен покрывать все оставшиеся элементы.

student_state JSON:
${JSON.stringify(state)}

${validationFeedback?.length ? `Ошибки предыдущего ответа, которые нужно исправить:\n${validationFeedback.map((item) => `- ${item}`).join("\n")}` : ""}

Верни строго JSON без markdown-блоков и без пояснений вокруг JSON:
{
  "analysis": {
    "summary": "короткий вывод о ситуации студента",
    "situation": "почему прогноз именно такой",
    "risks": ["конкретный риск"],
    "assumptions": ["допущение"],
    "confidence": 0.8
  },
  "forecast": {
    "status": "on_track|behind",
    "text": "человеческий прогноз",
    "requiredHoursPerWeek": 5
  },
  "calendar": [
    {"date": "YYYY-MM-DD", "action": "Дисциплина — Тема — Занятие/Конспект/Глоссарий/Тест", "time": "30 мин|45 мин|1 ч|1,5 ч|2 ч", "practiceRecommendation": "короткий практический совет по применению материала"}
  ],
  "today": {
    "date": "${state.semester.currentDate}",
    "items": ["конкретное действие на сегодня"],
    "time": "30 мин|45 мин|1 ч|1,5 ч|2 ч"
  },
  "progress": {
    "daysLeft": ${state.semester.daysLeft},
    "completedTopics": ${state.progress.completedTopics},
    "totalTopics": ${state.progress.totalTopics},
    "forecast": "on_track|behind"
  },
  "recommendations": ["конкретная рекомендация"],
  "markdown": "краткая версия плана в markdown"
}

Жесткие правила:
- forecast — это прогнозная модель до конца семестра. Сравни remainingItems, daysLeft, availableHoursUntilDeadline, estimatedHoursRemaining и темп студента. Дай короткий, понятный студенту вердикт: успевает ли он закрыть дисциплины в срок или нет.
- forecast.text должен объяснять причину прогноза за 1-2 предложения без шаблонных фраз. Если есть риск, явно укажи, что нужно изменить в нагрузке.
- analysis.risks должен содержать реальные причины риска: мало дней, много сложных тем, недостаточная недельная нагрузка, перегруз в отдельные дни.
- calendar должен покрыть каждый элемент из remainingItems. Не сокращай план до первых 5-30 пунктов.
- Порядок calendar должен быть результатом твоего анализа: учитывай сложность, дедлайн, связанность тем, когнитивную нагрузку и доступные дни. Не повторяй порядок remainingItems автоматически.
- calendar[].action должен содержать только учебный путь в формате "Дисциплина — Тема — Занятие/Конспект/Глоссарий/Тест". Не добавляй ссылки, советы и пояснения внутрь action.
- calendar[].time должен быть умным расчетом нагрузки по сложности темы, дедлайну и доступному времени. Не копируй автоматически предпочтение sessionDuration.
- calendar[].practiceRecommendation должен давать 1 конкретный практический способ закрепить материал: кейс, мини-задача, вопрос для самопроверки или применение к специальности студента. Не пиши общие фразы.
- recommendations должны анализировать ближайшую учебную неделю: какие дисциплины и темы идут рядом, как лучше усваивать материал, какие понятия стоит связать между собой и где применить знания на практике. Не пиши общие фразы про движение по календарю.
- progress.daysLeft, progress.completedTopics и progress.totalTopics должны строго совпадать со student_state.
- calendar[].date должен быть в формате YYYY-MM-DD, не раньше currentDate и не позже semester.endDate.
- Не используй шаблонные действия вроде "Зайти в LMS, пройти материал".
- Не придумывай новых дисциплин, дедлайнов или прогресса.
- Не давай ответы на тесты, только план обучения.`
